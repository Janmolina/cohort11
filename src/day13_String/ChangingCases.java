package day13_String;

public class ChangingCases {

	public static void main(String[] args) {


		String str = "Hello";
		System.out.println(str);
		
		str = str.toLowerCase();  // minuscula
		System.out.println(str);
		
		str = str.toUpperCase();  // mayuscula
		System.out.println(str);
		
		
				

	}

}
